	data = [6.360473  1.098654  0.273242; 
		5.477037  1.031857  0.228245 ;
		5.998487  0.828876  0.372581 ;
		3.244228  0.367452  0.327301 ;
		5.379480  0.560103  0.308363 ;
		3.648573  0.447176  0.239542 ;
		5.039962  1.030374  0.370597 ;
		4.369907  0.489844  0.342406 ;
		4.370628  0.512658  0.312935 ;
		4.479414  0.620200  0.368142 ;
		6.750024  0.700321  0.074164 ;
		8.025106  1.568598  0.024888 ];
	
	a = 5;
	b = 24;

	k = 1;
	for i = 1:length(data)
		vm = data(i,1);
		amp = sqrt(data(i,2)*data(i,2) + data(i,3)*data(i,3));
		vm_inf = vm - amp/2;
		vm_sup = vm + amp/2;

		if (a <= vm_inf && vm_sup <= b)
			est(k) = i;
			k = k + 1;
		endif
	endfor
